package Week3.Conditon;

public class Basic {
    public static void main(String[] args) {

        // 변수 선언
        boolean example = true;

        if(example) { // 참일 경우
            System.out.println("OK, true");
        } else { // 참이 아닐 경우(거짓일 경우)
            System.out.println("No, false");
        }

    }
}
